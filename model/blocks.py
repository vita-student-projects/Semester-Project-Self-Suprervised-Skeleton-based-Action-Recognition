class TemporalAttention(nn.Module):
    def __init__(self, dim, num_heads=8, qkv_bias=False, qk_scale=None, attn_drop=0., proj_drop=0.):
        super().__init__()
        self.num_heads = num_heads
        qk_dim = dim // num_heads

        self.scale = qk_scale or qk_dim ** -0.5

        self.attn_drop = nn.Dropout(attn_drop)
        self.proj = nn.Linear(dim, dim)
        self.qkv = nn.Linear(dim, dim * 3, bias=qkv_bias)   # (B, T, C) -> (B, T, 3C)
        self.proj_drop = nn.Dropout(proj_drop)


    def forward(self, x, seqlen=8):
        B, T, C = x.shape
        # (B, T, C) -> (B, T, 3C) -> (B, T, 3, #heads, C//#heads) -> (3, B, #heads, T, C//#heads)
        qkv = self.qkv(x).reshape(B, T, 3, self.num_heads, C // self.num_heads).permute(2, 0, 3, 1, 4)
        
        q, k, v = qkv[0], qkv[1], qkv[2]    # (B, #heads, T, C//#heads)
        x = self.forward_temporal(q, k, v, seqlen)

        x = self.proj(x)
        x = self.proj_drop(x)

        return x


    def forward_temporal(self, q, k, v, seqlen=8):
        B, _, T, Chead = q.shape    # (B, #heads, T, C//#heads)
        # (B, #heads, seqlen, T//seqlen, C//#heads) -> (B, #heads, T//seqlen, seqlen, C//#heads)
        qt = q.reshape(B, self.num_heads, seqlen, -1, Chead).permute(0, 1, 3, 2, 4) # B, #heads, T//seqlen, seqlen, Chead
        kt = k.reshape(B, self.num_heads, seqlen, -1, Chead).permute(0, 1, 3, 2, 4) 
        vt = v.reshape(B, self.num_heads, seqlen, -1, Chead).permute(0, 1, 3, 2, 4) 

        # (B, #heads, T//seqlen, seqlen, Chead) @ (B, #heads, T//seqlen, Chead, seqlen)
        attn = (qt @ kt.transpose(-2, -1)) * self.scale # B, #heads, T//seqlen, seqlen, seqlen
        attn = attn.softmax(dim=-1)
        attn = self.attn_drop(attn)

        x = attn @ vt   # B, #heads, T//seqlen, seqlen, C//#head
        # B, #heads, T//seqlen, seqlen, C//#head -> B, seqlen, T//seqlen, #heads, C//#heads
        x = x.permute(0, 3, 2, 1, 4).reshape(B, T, Chead*self.num_heads)    # B, T, Chead*#heads
        return x



